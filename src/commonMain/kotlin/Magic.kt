    private fun oppositeEdges(square: Square): Long {
        val rank = square.rank
        val file = square.file

        val left = Bitboards.FILE_A and Bitboards.RANK_1.inv() and Bitboards.RANK_8.inv()
        val right = Bitboards.FILE_H and Bitboards.RANK_1.inv() and Bitboards.RANK_8.inv()
        val bottom = Bitboards.RANK_1 and Bitboards.FILE_A.inv() and Bitboards.FILE_H.inv()
        val top = Bitboards.RANK_8 and Bitboards.FILE_A.inv() and Bitboards.FILE_H.inv()

        var edges = Bitboards.RANK_1 or Bitboards.RANK_8 or Bitboards.FILE_A or Bitboards.FILE_H

        if (rank == 0) {
            edges = edges and bottom.inv()
        }
        if (rank == 7) {
            edges = edges and top.inv()
        }
        if (file == 0) {
            edges = edges and left.inv()
        }
        if (file == 7) {
            edges = edges and right.inv()
        }

        return edges
    }

    fun getBishopAttacks(squareIdx: Int, allBlockers: Long): Long {
        val index =
